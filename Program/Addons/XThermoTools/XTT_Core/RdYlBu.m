function [ColorMap] = RdYlBu(ResColorMap)
%

ColorData = [0.647	0.000	0.149; ...
    0.843	0.188	0.153; ...
    0.957	0.427	0.263; ...
    0.992	0.682	0.380; ...
    0.996	0.878	0.565; ...
    1.000	1.000	0.749; ...
    0.878	0.953	0.973; ...
    0.671	0.851	0.914; ...
    0.455	0.678	0.820; ...
    0.271	0.459	0.706; ...
    0.192	0.212	0.584];

ColorData = flipud(ColorData);

Xi = 1:ResColorMap;
Step = (ResColorMap-1)/(size(ColorData,1)-1);
X = 1:Step:ResColorMap;

ColorMap = zeros(length(Xi),size(ColorData,2));
for i = 1:size(ColorData,2)
    ColorMap(:,i) = interp1(X',ColorData(:,i),Xi);
    % = polyval(P,);
end

return
